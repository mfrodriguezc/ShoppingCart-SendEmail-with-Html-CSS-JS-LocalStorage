function productCount(product){
    const inputBtn = document.getElementById('input1').value;
    const newInputBtn = parseInt(inputBtn);
    let total = newInputBtn;
    if(product == true){
     total = newInputBtn + 1;
    }
    if(product == false && newInputBtn > 1){
     total = newInputBtn - 1;
    }
    document.getElementById('quantity').innerText = document.getElementById('input1').value = total; 
 }
